function isLanguageDiverse(list) {
    let sorted = list.reduce((acc, curr) => {
        if (curr.language === "JavaScript") acc[0]++;
        if (curr.language === "Ruby") acc[1]++;
        if (curr.language === "Python") acc[2]++;
        return acc
    }, [0, 0, 0]).sort((a, b) => a - b);
    return sorted[2] / sorted[0] <= 2;
}

